    private void viewPayment() {
        try {
            ArrayList<SupplierPaymentDTO> getView=SupplierPaymentController.getSupplierPayment();
            DefaultTableModel dtm=(DefaultTableModel) tblPayment.getModel();
            if(getView!=null){
                for (SupplierPaymentDTO supplierPaymentDTO : getView) {
                    Object[] rowData={supplierPaymentDTO.getGrnId(),supplierPaymentDTO.getPayment(),supplierPaymentDTO.getSupOrderId()};
                    dtm.addRow(rowData);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewSupplierPayment.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
